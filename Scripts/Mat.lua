
local Mat = 
{
    -- ===========================================================================================================
    -- #region [CORE]
    -- ===========================================================================================================
    Load = function(self)
        self.drawable = love.graphics.newImage("Images/Background/mat.png")

        -- Components
        self.sprite = Sprite:New(
            self.drawable,
            { x = love.graphics.getWidth() / 2, y = love.graphics.getHeight() / 2, z = 0 },
            0,
            1,
            DrawLayers.PerformanceMat,
            true,
            { x = 0.5, y = 0.5 }
        )
    end,
    -- ===========================================================================================================
    -- #region [EXTERNAL]
    -- ===========================================================================================================

    OnStartPerform = function(self)
        DrawSystem:AddDrawable(self.sprite)
    end,

    OnStopPerform = function(self)
        DrawSystem:RemoveDrawable(self.sprite)
    end,

    -- ===========================================================================================================
    -- #region [INTERNAL]
    -- ===========================================================================================================
    -- ===========================================================================================================
    -- #region [PUBLICHELPERS]
    -- ===========================================================================================================
    -- ===========================================================================================================
    -- #endregion
}

return Mat